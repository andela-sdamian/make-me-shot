$(document).ready(function(){
    $('#short_it').click(function(e){
        var url = $('#link_long').val();     
        if(!isUrlValid(url)) {
            Materialize.toast('Please enter a valid url', 3000);
            e.preventDefault();
        }
        
        if (isSameOrigin(url)) {
            Materialize.toast("Sorry, we don't allow redirect to same origin!", 3000);
            e.preventDefault();
        }
    });
    
    $('#reg').click(function(e){
        if (!isFullNameValid()) {
            Materialize.toast('Enter a valid first name!',3000);
            e.preventDefault();
        }
   
        $('#new_user').submit();
    });
});

